const { Message } = require('discord.js')

module.exports = {
    name : 'addrole',
    usage:"@User (Rolle)",
    description:"Mit diesem command kann jeder, der die Berechtigung Rollen verwalten hat, einem Mitglied eine Rolle hinzuf체gen",
    run : async(client, message, args) => {
        //lets use parameters (optional)
        /**
         * @param {Message} message
         */
        //so firstly we will check whether the author of the message has permissions
        //this line means if the author doesn't have manage roles permission it will stop the process and send the following text
        if(!message.member.hasPermission("MANAGE_ROLES")) return message.channel.send('Du hast keine Berechtigung daf체r')
        //next we define some variables
        const target = message.mentions.members.first() //member = mentions
        if(!target) return message.channel.send('Kein Mitglied ausgew채hlt ') //when no member is pinged
        const role = message.mentions.roles.first() // roles = mentions
        if(!role) return message.channel.send('Keine Rolle ausgew채hlt') //when no role is specified or pinged
        //now the code!
        await target.roles.add(role) // adding the role to the user
        message.channel.send(`${target.user.username} hat eine Rolle erhalten`)
    }
}
